package org.eclipse.birt.report.engine.api;

/**
 * a class that wraps around an identifier for a report component
 */
public class ComponentID {
	protected long componentID;

	/**
	 * Get an identifier for a report component.
	 * 
	 * @return componentID
	 */
	public long getID() {
		return componentID;
	}
}
